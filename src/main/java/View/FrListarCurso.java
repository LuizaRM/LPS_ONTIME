    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Curso curso = new Curso();
        int row = tblListarCursos.getSelectedRow();
        
        String id = tblListarCursos.getModel().getValueAt(row, 0).toString();
        curso.setId(Integer.parseInt(id));

        String nome = tblListarCursos.getModel().getValueAt(row, 1).toString();
        curso.setNome(nome);

        cursoController = new CursoController(2, curso);
        this.dispose();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       int opcao = JOptionPane.showConfirmDialog(null, "Realmente deseja excluir o curso?");
        // 0=yes, 1=no, 2=cancel    
        
        if (opcao == 0) {
            int row = tblListarCursos.getSelectedRow();
            String value = tblListarCursos.getModel().getValueAt(row, 0).toString();
            cursoController.excluir(Integer.parseInt(value));
            this.dispose();
            FrListarCurso frlist = new FrListarCurso();
            frlist.setVisible(true);

        }

    }//GEN-LAST:event_btnExcluirActionPerformed

