    public void carregarListaCursos() {

        ArrayList<Curso> lista = cursoController.getListaCurso();
        String[] columnNames = {"Id Curso", "Nome do Curso"};
        tableModel = new DefaultTableModel(columnNames, 0);

        for (int i = 0; i < lista.size(); i++) {
            int id = lista.get(i).getId();
            String nome = lista.get(i).getNome();

           
            String[] data = {Integer.toString(id), nome};

            tableModel.addRow(data);
        }
        tblListarCursos.setModel(tableModel);
        spnListarCurso.setViewportView(tblListarCursos);
        //deixando na tabela e removendo da visualização

//
        tblListarCursos.removeColumn(tblListarCursos.getColumnModel().getColumn(0));

    }

